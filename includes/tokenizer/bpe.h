/**
 * TikToken's BPE tokenizer is a type of byte pair encoding that is used to tokenize text into tokens.
 * It is a simple and efficient tokenization method that is used in many NLP models.
 *
 * The tokenizer works by merging the most frequent byte pairs in the text.
 * It does this by creating a frequency list of all the byte pairs in the text.
 * The most frequent byte pair is then merged and the process is repeated until the desired number of tokens is reached.
 *
 * The tokenizer uses a special byte to represent the end of a token.
 */

